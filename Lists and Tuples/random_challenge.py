data = [
    "Andromeda - Shrub",
    "Bellflower - Flower",
    "China Pink - Flower",
    "Daffodil - Flower",
    "Evening Primrose - Flower",
    "French Marigold - Flower",
    "Hydrangea - Shrub",
    "Iris - Flower",
    "Japanese Camellia - Shrub",
    "Lavender - Shrub",
    "Lilac - Shrub",
    "Magnolia - Shrub",
    "Peony - Shrub",
    "Queen Anne's Lace - Flower",
    "Red Hot Poker - Flower",
    "Snapdragon - Flower",
    "Sunflower - Flower",
    "Tiger Lily - Flower",
    "Witch Hazel - Shrub",
]

flowers = []
shrubs = []

print(flowers)

for plant in data:
    if "Flower" in plant:
        flowers.append(plant)
    else:
        shrubs.append(plant)

flowers = [item.replace("- Flower", "").strip() for item in flowers]
shrubs = [item.strip("- Shrub").strip() for item in shrubs]

print(flowers)
print(shrubs)
